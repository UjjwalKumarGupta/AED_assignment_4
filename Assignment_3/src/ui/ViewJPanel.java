    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblPerson.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel)tblPerson.getModel();
            person p = (person)model.getValueAt(selectedRowIndex, 0);

            p.setName(txtName.getText());
            p.setCommunity(txtCommunity.getText());
            p.setAge(Integer.parseInt(txtAge.getText()));
            p.setPersonID(Integer.parseInt(txtId.getText()));
            p.setCity(txtCity.getText());
            p.setHouse(txtHouse.getText());
            personD.updatePerson(selectedRowIndex, p);
            JOptionPane.showMessageDialog(this, "Person Details Updated");

            if(selectedRowIndex >= 0){

                model.setValueAt(txtName.getText(),selectedRowIndex, 0);
                model.setValueAt(txtId.getText(),selectedRowIndex, 1);
                model.setValueAt(txtAge.getText(),selectedRowIndex, 2);
                model.setValueAt(txtCity.getText(),selectedRowIndex, 3);
                model.setValueAt(txtCommunity.getText(),selectedRowIndex, 4);
                model.setValueAt(txtHouse.getText(),selectedRowIndex, 5);
            }
            else{

                JOptionPane.showMessageDialog(this, "Error updating");
            }
           
        
       
        
        
           

    }//GEN-LAST:event_btnUpdateActionPerformed

