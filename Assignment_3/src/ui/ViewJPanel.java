private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblPerson.getModel();
        model.setRowCount(0);
        
        for(person p : personD.getPersonD()){
            
            Object[] row = new Object[6];
            row[0] = p;
            row[1] = p.getPersonID();
            row[2] = p.getAge();
            row[3] = p.getCity();
            row[4] = p.getCommunity();
            row[5] = p.getHouse();
            
            
            model.addRow(row);
        }
    }

